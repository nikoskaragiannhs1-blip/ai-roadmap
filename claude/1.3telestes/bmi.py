# Δεδομένα χρήστη
onoma = "Nikos"
varos = 80      # κιλά
ypsos = 1.75    # μέτρα

# Υπολογισμός BMI
# Τύπος: βάρος διαιρεμένο με ύψος στο τετράγωνο
bmi = varos / (ypsos ** 2)

# Στρογγυλοποίηση στα 2 δεκαδικά
bmi = round(bmi, 2)

# Αποτέλεσμα
print("========== BMI CALCULATOR ==========")
print("Όνομα:", onoma)
print("Βάρος:", varos, "kg")
print("Ύψος:", ypsos, "m")
print("BMI:", bmi)

# Κατηγορία
if bmi < 18.5:
    print("Κατηγορία: Λιποβαρής")
elif bmi < 25:
    print("Κατηγορία: Φυσιολογικό βάρος ✅")
elif bmi < 30:
    print("Κατηγορία: Υπέρβαρος")
else:
    print("Κατηγορία: Παχυσαρκία")

print("=====================================")